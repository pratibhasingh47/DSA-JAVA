import java.util.*;

class HashSetCreateDemo {
    public static void main(String[] args) {
        // Create a hash set.
        Set<String> hs = new HashSet<>();

        // Add elements to the hash set.
        hs.add("India");
        hs.add("Japan");
        hs.add("Australia");
        hs.add("Bangladesh");
        hs.add("Pakistan");
        hs.add("Nepal");

        // Print the hash set.
        System.out.println(hs);
    }
}
